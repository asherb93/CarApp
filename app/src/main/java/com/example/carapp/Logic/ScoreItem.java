package com.example.carapp.Logic;

public class ScoreItem {

    String name;
    int score;

    public void ScoreItem(){

    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getScore() {
        return score;
    }

    public void setScore(int score) {
        this.score = score;
    }
}
